int check(int z, int y, int x){ // BFS시 배열 나가는지 확인

    if(z >= height || y >= length || x >= width){
        return 0;
    }

    if(z < 0 || y < 0 || x < 0){
        return 0;
    }

    if(map[z][y][x] == 1){
        return 0;
    }

    return 1;
}

int BFS(){  // BFS 탐색 함수

    front = 0;
    rear = 1;

    queue[0].z = usr_z;
    queue[0].y = usr_y;
    queue[0].x = usr_x;
    queue[0].move_cnt = 0;

    visited[usr_z][usr_y][usr_x] = 1;

    while(front < rear){
        _data cur = queue[front];

        if(map[cur.z][cur.y][cur.x] == EXIT){

            for(int i = 0; i < cur.move_cnt; i++){
                printf("%d ", cur.move[i]);
            }
            print_result(cur.move, cur.move_cnt);

            return 1;
        }


        int loop_num = 4;
        if(map[cur.z][cur.y][cur.x] == stairs){
            loop_num = 6;
        }

        for(int i = 0; i < loop_num; i++){

            int pre_z = cur.z + z_move[i];
            int pre_y = cur.y + y_move[i];
            int pre_x = cur.x + x_move[i];

            if(check(pre_z, pre_y, pre_x) && !visited[pre_z][pre_y][pre_x]){
                _data next = cur;

                next.z = pre_z;
                next.y = pre_y;
                next.x = pre_x;

                next.move[next.move_cnt] = i + 1;
                next.move_cnt++;

                visited[pre_z][pre_y][pre_x] = 1;

                queue[rear] = next;
                rear++;
            }
        }

        front++;
    }

    printf("fail to escape\n");
    return 0;
}

